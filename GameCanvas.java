        public void addKeyBindings() {
                this.setFocusable(true);
                this.addKeyListener(new KeyAdapter() {
                        @Override
                        public void keyPressed(KeyEvent e) {
                                switch (e.getKeyCode()) {
                                        case KeyEvent.VK_W:
                                                player.move("UP");
                                                break;
                                        case KeyEvent.VK_S:
                                                player.move("DOWN");
                                                break;
                                        case KeyEvent.VK_A:
                                                player.move("LEFT");
                                                break;
                                        case KeyEvent.VK_D:
                                                player.move("RIGHT");
                                                break;
                                }
                        }

                        @Override
                        public void keyReleased(KeyEvent e) {
                                player.stop();
                        }
                });
        }

